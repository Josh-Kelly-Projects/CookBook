    private void btnSortActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSortActionPerformed
    {//GEN-HEADEREND:event_btnSortActionPerformed
        pnlIngrs.repaint();
        pnlIngrs.removeAll();
        pnlRecip.removeAll();
        pnlRecip.repaint();

        ingrsArr = new IngredientsArr(cmbMealPicker.getSelectedItem().toString());

        Ingredients[] ingrs;
        ingrs = ingrsArr.getIngArr();
        while (a < ingrsArr.getSize())
        {
            String d = ingrs[a].getIngName();
            cbArr[a] = new javax.swing.JCheckBox(d);
            pnlIngrs.add(cbArr[a], new org.netbeans.lib.awtextra.AbsoluteConstraints(10, 25 * (a), 200, 20));
            cbArr[a].setVisible(true);
            a++;
        }

        btnContinue.setEnabled(true);
        btnSelectFridge.setEnabled(true);
        pnlIngrs.updateUI();
    }//GEN-LAST:event_btnSortActionPerformed


    private void btnContinueActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnContinueActionPerformed
    {//GEN-HEADEREND:event_btnContinueActionPerformed
        pnlRecip.repaint();
        pnlIngrs.repaint();

        Ingredients[] ingr = ingrsArr.getIngArr();
        Ingredients[] ingr2 = new Ingredients[ingrsArr.getSize()];
        int b = 0, c = 0;

        for (int i = 0; i < a; i++)
        {
            if (cbArr[b].isSelected() == true)
            {
                ingr2[c] = ingr[b];
                b++;
                c++;
            } else
            {
                b++;
            }
        }
        IngredientsArr ingrsArr2 = new IngredientsArr(ingr2, c);
        RecipeArr resArr = new RecipeArr(ingrsArr2, c, cmbMealPicker.getSelectedItem().toString());
        Recipe[] resp = resArr.getResArr();
        javax.swing.JButton[] btnArr = new javax.swing.JButton[2000];
        int a = 0;
        while (a < resArr.getSize())
        {
            String d = resp[a].getResName();
            btnArr[a] = new javax.swing.JButton(d);
            pnlRecip.add(btnArr[a], new org.netbeans.lib.awtextra.AbsoluteConstraints(10, 35 * (a), 160, 30));
            btnArr[a].setVisible(true);
            btnArr[a].setBackground(Color.yellow);

            btnArr[a].addActionListener(new java.awt.event.ActionListener()
            {
                @Override
                public void actionPerformed(java.awt.event.ActionEvent evt)
                {
                    try
                    {
                        ResultSet rs = db.query("SELECT id FROM tblrecipies WHERE recipe LIKE '" + evt.getActionCommand() + "'");
                        rs.next();
                        int id = rs.getInt("id");
                        Recipe rec = new Recipe(evt.getActionCommand(), id);
                        new BookGUI(user, rec).setVisible(true);
                    } catch (SQLException ex)
                    {
                        Logger.getLogger(HomeGUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            });
            a++;
        }
        pnlIngrs.removeAll();
        btnSelectFridge.setEnabled(false);
        pnlIngrs.repaint();
        pnlRecip.updateUI();
    }//GEN-LAST:event_btnContinueActionPerformed


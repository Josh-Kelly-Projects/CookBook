    private void btnSelectFridgeActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnSelectFridgeActionPerformed
    {//GEN-HEADEREND:event_btnSelectFridgeActionPerformed
        try
        {
            ResultSet rs = db.query("SELECT poolID FROM tblusers WHERE userName LIKE '" + user.getUserName() + "'");
            rs.next();
            String pool = rs.getString("poolID");
            InventoryArr invent = new InventoryArr(pool);
            Inventory[] inArr = invent.getInvenArr();
            int b = 0;
            while (cbArr[b].isVisible() == true)
            {
                for (int i = 0; i < invent.getSize(); i++)
                {
                    if (cbArr[b].getText().equalsIgnoreCase(inArr[i].getIngname()))
                    {
                        cbArr[b].setSelected(true);
                    }
                }
                b++;
            }
        } catch (SQLException ex)
        {
            Logger.getLogger(HomeGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSelectFridgeActionPerformed

